# Задача-1: Дано произвольное целое число, вывести самую большую цифру этого числа.
# Например, дается x = 58375.
# Нужно вывести максимальную цифру в данном числе, т.е. 8.
# Подразумевается, что мы не знаем это число заранее.
# Число приходит в виде целого беззнакового.
# Подсказки:
# * постарайтесь решить задачу с применением арифметики и цикла while;
# * при желании и понимании решите задачу с применением цикла for.


def max_digit_math(number):
    """
    Распертрушиваем число на цифры с помощью арифметики, попутно
    проверяя каждую цифру на максимум
    """
    num = number
    result = 0
    while num:
        if result < num % 10:
            result = num % 10
        num //= 10
    return result


def max_digit_list(number):
    """
    Делаем из числа список составных цифр и возвращаем максимальный элемент
    """
    return max([int(x) for x in str(number)])


if __name__ == '__main__':
    print('*' * 70)
    print('Нахождение максимальной цифры из записи целого числа')
    in_number = int(input('Введите целое число: '))
    print('-' * 70)
    print('Арифметика и сравнение')
    print('Максимальная цифра в ведённом числе:', max_digit_math(in_number))
    print('-' * 70)
    print('Встроенные функции для ленивых')
    print('Максимальная цифра в ведённом числе:', max_digit_list(in_number))
    print('*' * 70)


