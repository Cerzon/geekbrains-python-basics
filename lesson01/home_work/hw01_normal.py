# Задача-3: Напишите программу, вычисляющую корни квадратного уравнения вида
# ax² + bx + c = 0.
# Коэффициенты уравнения вводятся пользователем.
# Для вычисления квадратного корня воспользуйтесь функцией sqrt() модуля math:
# import math
# math.sqrt(4) - вычисляет корень числа 4

from math import sqrt

def square_equation_solver(a, b, c):
    """
    Возвращает кортеж из двух элементов, содержащих либо
    вещественные корни уравнения, либо сообщение об
    отсутствии вещественных корней
    """
    d = b**2 - 4 * a * c
    if d < 0:
        return None, 'Уравнение не имеет вещественных корней'
    elif d == 0:
        return -b / (2 * a), None
    else:
        return (-b - sqrt(d)) / (2 * a), (-b + sqrt(d)) / (2 * a)


if __name__ == '__main__':
    print('*' * 70)
    print('Решаем квадратное уравнение ax² + bx + c = 0')
    a = int(input('Введите значение коэффициента a: '))
    b = int(input('Введите значение коэффициента b: '))
    c = int(input('Введите значение коэффициента с: '))
    print('-' * 70)
    x1, x2 = square_equation_solver(a, b, c)
    if x1 is None:
        print(x2)
    elif x2 is None:
        print('Уравнение имеет один корень x =', x1)
    else:
        print('x1 =', x1, ' , x2 =', x2)
    print('*' * 70)
